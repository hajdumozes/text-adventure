package objects;

public abstract class BattlefieldObject {
    private String name;

    public BattlefieldObject(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
